class StepCounter:
    def __init__(self, start, end, step):
        self.current = start
        self.end = end
        self.step = step
    
    def __iter__(self):
        return self
    
    def __next__(self):
        if self.current <= self.end:
            val = self.current
            self.current += self.step
            return val
        else:
            raise StopIteration

