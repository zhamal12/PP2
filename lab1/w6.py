#Data Types

x = "Hello World"
print(x)
print(type(x))

x1 = 20
print(x1)
print(type(x1))

x2 = 20.7
print(x2)
print(type(x2))

x3 = 1j 
print(x3)
print(type(x3))

x4 = ["apple", "banana", "cherry"]
print(x4)
print(type(x4))

x5 = ("apple", "banana", "cherry")
print(x5)
print(type(x5))

x6 = range(6)
print(x6)
print(type(x6))

x7 = {"name" : "John", "age" : 30}
print(x7)
print(type(x7))

x8 = {"apple", "banana", "cherry"}
print(x8)
print(type(x8))

x9 = frozenset({"apple", "banana", "cherry"})
print(x9)
print(type(x9))

x10 = True
print(x10)
print(type(x10))

x11 = b"Hello"
print(x11)
print(type(x11))

x12 = bytearray(6)
print(x12)
print(type(x12))

x13 = memoryview(bytes(5))
print(x13)
print(type(x13))

x14 = None 
print(x14)
print(type(x14))
